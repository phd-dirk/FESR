!     Subroutine to provide weights of contour integration
!     in the correct format to be included in this module
!     Weights multiplied by factor 1.d3!!!
      subroutine calcgauss()

      implicit none
      integer :: n

!     Initialise Gauss integration
      call gauleg(-pi,pi,yy,wg,ngau)

      open (unit=9, file='weights.out')

      do n = 1,ngau-2,2
         write(9,FMT='(A8,D28.22,A4,D28.22,A2)') "       &",&
                &yy(n), ",   ", yy(n+1), ",&"
      end do
      write(9,FMT='(A8,D28.22)') "       &", yy(ngau)

      write(9,*) ""

      do n = 1,ngau-2,2
         write(9,FMT='(A8,D28.22,A4,D28.22,A2)') "       &",&
                &wg(n), ",   ", wg(n+1), ",&"
      end do
      write(9,FMT='(A8,D28.22)') "       &", wg(ngau)

      close (9)

      return
      end subroutine calcgauss

